import { JWT_SECRET } from "../../config.js";
import jwt from "jsonwebtoken";

export const checkToken = (token) => {
  return new Promise((resolve, reject) => {
    jwt.verify(token, JWT_SECRET, (err, decoded) => {
      if (err) {
        reject(err);
      } else {
        resolve(decoded);
      }
    });
  });
};
